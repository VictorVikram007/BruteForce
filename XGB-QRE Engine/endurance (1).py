import pandas as pd
import numpy as np

num_batteries = 50
cycles_per_battery = 1500
initial_capacity = 35

data = []

for b in range(num_batteries):

    capacity = initial_capacity

    # Each battery behaves differently
    battery_factor = np.random.uniform(0.8, 1.2)

    for i in range(1, cycles_per_battery + 1):

        temp = np.random.uniform(25, 40)
        peak_temp = temp + np.random.uniform(5, 15)
        dod = np.random.uniform(60, 95)

        discharge_c = np.random.uniform(1.0, 3.0)
        charge_c = np.random.uniform(0.5, 1.5)

        # New physics-inspired degradation factor based on peak_temp
        # Assuming degradation accelerates significantly above 35 degrees Celsius
        temp_threshold = 35
        # 5% increase in degradation for each degree above 35
        temp_factor = 1.0 + max(0, (peak_temp - temp_threshold) * 0.05)

        # Updated stress calculation incorporating temp_factor
        stress = (
            (0.00005 * dod +
             0.0001 * discharge_c +
             0.00008 * (peak_temp - 30))
            * battery_factor * temp_factor # Apply temp_factor here
        )

        degradation = stress + np.random.normal(0, 0.0001)

        capacity -= degradation * initial_capacity
        capacity = max(capacity, initial_capacity * 0.7)

        soh = (capacity / initial_capacity) * 100

        data.append([
            b, i, temp, peak_temp, dod,
            discharge_c, charge_c,
            capacity, soh
        ])

df = pd.DataFrame(data, columns=[
    "Battery_ID",
    "Cycle_Index",
    "Avg_Ambient_Temp",
    "Peak_Cell_Temp",
    "Daily_DoD",
    "Max_Discharge_C_Rate",
    "Max_Charge_C_Rate",
    "Present_Capacity",
    "State_of_Health"
])

# Add Temp_Stress as a new feature
df["Temp_Stress"] = df["Peak_Cell_Temp"] - df["Avg_Ambient_Temp"]

# Compute RUL per battery
# Initialize Target_RUL_Cycles with NaN to distinguish from 0
df["Target_RUL_Cycles"] = np.nan

for b_id in df["Battery_ID"].unique():
    battery_data = df[df["Battery_ID"] == b_id].copy()

    # Find the first cycle index where SOH drops to or below 80%
    eol_condition = battery_data["State_of_Health"] <= 80
    eol_cycles = battery_data[eol_condition]["Cycle_Index"]

    if not eol_cycles.empty:
        eol_cycle_index = eol_cycles.min()

        # Calculate RUL for all cycles of this battery up to and including the EOL cycle
        valid_cycles = battery_data[battery_data["Cycle_Index"] <= eol_cycle_index]
        df.loc[valid_cycles.index, "Target_RUL_Cycles"] = eol_cycle_index - valid_cycles["Cycle_Index"]
    # If eol_cycles is empty, the battery never reached 80% SOH within the simulated cycles.
    # These rows will remain with NaN RUL and will be dropped later.

# Drop rows where RUL is NaN (i.e., batteries that didn't reach EOL within simulation)
df = df.dropna(subset=['Target_RUL_Cycles']).copy()

# Ensure RUL is non-negative and integer
df['Target_RUL_Cycles'] = df['Target_RUL_Cycles'].astype(int)
df['Target_RUL_Cycles'] = df['Target_RUL_Cycles'].apply(lambda x: max(0, x))

df.to_csv("battery_dataset_final.csv", index=False)

